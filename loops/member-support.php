<h3>Promotions</h3>

<h4>What is a Promotion?</h4>
<p>A promotion is really just an offering or sales announcement your company might be running. If you were a pizzeria for example, you could be running a promotion for $10 plain pizzas on Mondays.</p>
<p>Promotions can include the following info:</p>
<ul>
	<li>A title</li>
	<li>Details (a paragraph of copy)</li>
	<li>Link</li>
	<li>Banner Ad (300 x 250 pixel image)</li>
</ul>
<p>The banner ad is optional. If you provide it, it puts your banner ad into the general rotation of banner ads across the site.</p>

<h4>Adding a Promotion</h4>
<p>To add a promotion:</p>
<ol>
	<li>Log in and click to edit your page</li>
	<li>Scroll to the bottom, and click on 'Add a Promotion' button under the Promotions heading</li>
	<li>Add your title, details, and link, and banner ad if you have it (you can always go in and edit or add stuff later)</li>
	<li>Click the update button to save your page</li>
</ol>
<p>You can always take a look at all of the promotions you have running on the <a href="?content-promotions">member dashboard</a>.</p>

