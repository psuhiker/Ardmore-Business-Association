<h3>Search Engine Optimization (SEO)</h3>

<h4>What is a SEO?</h4>
<p>SEO is the practice of optimizing your website copy so that Google and other search engines match up user searches to your pages.</p>
<p>SEO is a complex process with many nuances. However, there are three basic tags that you can use on your business' page:</p>
<ul>
	<li><strong>Title Tag</strong> This is the title of a page, ideally your business name and main focus. Something like, 'Pickel Web Design | Custom Web Development'</li>
	<li><strong>Description Tag</strong> This is a short 1-2 sentences that is an elevator pitch of your company. It appears on google search results as the copy that is shown to entice readers to click that particular search result.</li>
	<li><strong>Keywords</strong> These are topics your company is about. Something like 'Web Design, Web Development, Ardmore, Custom Graphic Design'</li>
</ul>
<p>It is also helpful to continue using these keywords in your company description.</p>

<h4>Updating Your SEO Tags</h4>
<p>To update your SEO tags:</p>
<ol>
	<li>Log in and click to edit your page</li>
	<li>Scroll to the bottom, and enter your preferred Title, Description, and Keyword tags</li>
	<li>Click the update button to save your page</li>
</ol>