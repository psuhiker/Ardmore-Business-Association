<h3>Photo Galleries</h3>

<p>You can upload photo galleries to your business page quickly and easily. To do so, first gather up your photos (you can have up to 10 at a time). It's best if images are:</p>
<ul>
	<li>.JPG, .GIF, or .PNG format</li>
	<li>Around 400kb or less in file size (there is a max file size of 2mb, but those take a while to download!)
</li>
<h4>Adding Photos</h4>
<p>To create a gallery and add photos:</p>
<ol>
	<li>Log in and click to edit your page</li>
	<li>Scroll to the bottom, and click on 'Add a Photo' button under the Photo Gallery heading</li>
	<li>In the new field that appeared, click on the 'Add Image' button</li>
	<li>Drag and drop your photos or click the 'Select Files' button to upload new photos (you can also click on the 'Media Library' tab at the top to toggle to photos you've previously uploaded)</li>
	<li>After the image has uploaded, click on the 'Select Image' button on the lower right hand corner</li>
	<li>After clicking the 'Select Image' button, you can give your image a caption (optional)</li>
	<li>To upload additional photos, repeat steps 1-6</li>
	<li>Once all your desired photos are uploaded, you can reorder them &mdash; click on a number next to a photo and drag them up or down to reorder</li>
</ol>
<h4>Editing an Existing Gallery</h4>
<p>If you have a gallery already created, you can remove older images and upload new ones.</p>
<p>To remove images:</p>
<ol>
	<li>Hover over the row that contains the image you would like to remove</li>
	<li>A (-) sign appears to the right &mdash; click it to remove the image</li>
	<li>Repeat as needed &mdash; once all of the photos you wish to remove are removed, you can add new ones using the steps outlined above</li>
</ol>

